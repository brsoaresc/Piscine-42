int	ft_sqrt(int nb)
{
	int	a;

	a = 1;
	if (a >= 46341)
		return (0);
	if (nb > 0)
	{
		while (a * a <= nb)
		{
			if (a * a == nb)
				return (a);
			a++;
		}
	}
	return (0);
}
