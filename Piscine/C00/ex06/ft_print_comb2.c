#include <unistd.h>

void	ft_print_comb2(void)
{
	char	x[5];
	char	a;
	char	b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			x[0] = '0' + (a / 10);
			x[1] = '0' + (a % 10);
			x[2] = ' ';
			x[3] = '0' + (b / 10);
			x[4] = '0' + (b % 10);
			write(1, &x, 5);
			if (a != 98 || b != 99)
				write (1, ", ", 2);
			b++;
		}
		a++;
	}
}

int	main(void)
{
	ft_print_comb2();
	return (0);
}
