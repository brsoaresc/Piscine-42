#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	a;

	a = 0;
	if (argc ==2)
	{
		while (argv[1][a] != '\0')
		{
			if (a % 2 == 0)
			{
				write(1, &argv[1][a], 1);
			}
			a++;
		}
		write (1, "\n", 1);
	}
}
